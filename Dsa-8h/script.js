// 1 --> findout the sum of the numbers of n ;

function sumNum(nums){
  let sum= 0;
  for(let i = 1 ; i <= nums ; i++){
    sum += i;
  }
  return sum;
};

console.log(sumNum(5));
console.log(sumNum(10));
console.log(sumNum(8));

